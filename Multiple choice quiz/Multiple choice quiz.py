import random

def generate_questions():
    # なぞなぞリスト（問題、選択肢、正解のインデックス）
    riddles = [
        ("パンはパンでも食べられないパンは？", ["フライパン", "食パン", "メロンパン", "カレーパン"], 1),
        ("朝は四本足、昼は二本足、夜は三本足のものは？", ["犬", "人間", "猫", "椅子"], 2),
        ("世界一長い川は？", ["ナイル川", "アマゾン川", "ミシシッピ川", "黄河"], 1),
        ("鳴き声が「モー」の動物は？", ["犬", "猫", "牛", "鳥"], 3),
        ("冬になると葉っぱが落ちる木は？", ["桜", "松", "杉", "楓"], 4),
        ("赤ちゃんが最初に覚える言葉は？", ["ママ", "パパ", "ワンワン", "ブーブー"], 1),
        ("火を使わずに作れる料理は？", ["サラダ", "カレー", "ラーメン", "焼肉"], 1),
        ("日本で一番高い山は？", ["富士山", "北岳", "槍ヶ岳", "白馬岳"], 1),
        ("1年で一番短い月は？", ["1月", "2月", "6月", "12月"], 2),
        ("卵から生まれる動物は？", ["犬", "猫", "鳥", "馬"], 3),
        ("夜になると光るものは？", ["太陽", "月", "星", "雲"], 2),
        ("日本の首都はどこ？", ["大阪", "京都", "東京", "名古屋"], 3),
        ("水の中で呼吸できる動物は？", ["犬", "魚", "鳥", "猫"], 2),
        ("空を飛べるものは？", ["飛行機", "車", "船", "電車"], 1),
        ("お正月に食べるものは？", ["おせち料理", "カレー", "ラーメン", "寿司"], 1),
        ("夏に食べたくなるものは？", ["アイスクリーム", "鍋", "焼肉", "おでん"], 1),
        ("雨の日に使うものは？", ["傘", "帽子", "手袋", "靴"], 1),
        ("学校で使うものは？", ["ノート", "スリッパ", "テレビ", "冷蔵庫"], 1),
        ("動物園にいる動物は？", ["ライオン", "犬", "猫", "馬"], 1),
        ("冬に降るものは？", ["雪", "雨", "霧", "風"], 1)
    ]

    # ランダムに問題を選択（重複なし）
    return random.sample(riddles, 4)  # 4問をランダムに選択

def quiz():
    score = 0
    questions = generate_questions()  # 重複しない問題を取得

    print("なぞなぞクイズへようこそ！")
    print("正しい答えを番号で選んでください。\n")

    for i, (question, options, correct_index) in enumerate(questions):
        print(f"問題 {i + 1}: {question}")
        for j, option in enumerate(options, 1):
            print(f"{j}. {option}")

        try:
            answer = int(input("あなたの答え: "))
            if answer == correct_index:
                print("正解！\n")
                score += 1
            else:
                print(f"不正解。正しい答えは「{options[correct_index - 1]}」です。\n")
        except (ValueError, IndexError):
            print("無効な入力です。選択肢の番号を入力してください。\n")

    print(f"クイズ終了！あなたのスコアは {score}/{len(questions)} です。")
    if score == len(questions):
        print("素晴らしい！全問正解です！")
    elif score >= len(questions) * 0.7:
        print("よくできました！")
    elif score >= len(questions) * 0.4:
        print("まあまあです。もう少し頑張りましょう！")
    else:
        print("もっと練習が必要ですね。")

if __name__ == "__main__":
    quiz()